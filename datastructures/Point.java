package datastructures;

public class Point {
	private int x1;
	private int x2;
	private int y;
	private int h;
	
	public Point(int x1, int x2, int y, int h) {
		this.x1 = x1;
		this.x2 = x2;
		this.y = y;
		this.h = h;
	}
	public int getX1() {
		return x1;
	}
	public void setX1(int x1) {
		this.x1 = x1;
	}
	public int getX2() {
		return x2;
	}
	public void setX2(int x2) {
		this.x2 = x2;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getH() {
		return h;
	}
	public void setH(int h) {
		this.h = h;
	}
	
}
